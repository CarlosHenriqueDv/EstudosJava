package br.com.estudojava.testes;

public class ProvaVida {

    private String local;
    private String motivoRealizacao;
    private String grauSatisfacao;
    private String conclusao;

    public String getLocal() {
        return local;
    }

    public void setLocal(String local) {
        this.local = local;
    }

    public String getMotivoRealizacao() {
        return motivoRealizacao;
    }

    public void setMotivoRealizacao(String motivoRealizacao) {
        this.motivoRealizacao = motivoRealizacao;
    }

    public String getGrauSatisfacao() {
        return grauSatisfacao;
    }

    public void setGrauSatisfacao(String grauSatisfacao) {
        this.grauSatisfacao = grauSatisfacao;
    }

    public String getConclusao() {
        return conclusao;
    }

    public void setConclusao(String conclusao) {
        this.conclusao = conclusao;
    }
}
